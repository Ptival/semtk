package com.ge.research.semtk.services.nodegroupStore.service;

import org.json.simple.JSONObject;
import org.json.simple.parser.JSONParser;
import org.json.simple.parser.ParseException;

import com.ge.research.semtk.utility.LocalLogger;

public class StoreNodeGroupRequest {

	private String jsonRenderedNodeGroup;
	private String name;
	private String comments;
	private String creator;
	// creation date will be auto-generated by the service
		
	public JSONObject getJsonNodeGroup(){
	
		JSONParser prsr = new JSONParser();
		JSONObject retval = null;
		
		LocalLogger.logToStdErr("----------START MESSAGE-------------");
		
		LocalLogger.logToStdErr("Store node group: ");
		LocalLogger.logToStdErr(jsonRenderedNodeGroup);
		
		LocalLogger.logToStdErr("----------END MESSAGE---------------");
		
		
		try {
			retval = (JSONObject) prsr.parse(this.jsonRenderedNodeGroup);
		} catch (ParseException e) {
			// TODO Auto-generated catch block
			LocalLogger.printStackTrace(e);
		}
		return retval;
	}

	/**
	 * Validate the request.  Throw exception if there is a problem.
	 */
	public void validate() throws Exception {
		if(getName() == null || getName().trim().isEmpty()){
			throw new Exception("Invalid request to store node group: ID is not provided, or is empty");
		}
		if(getComments() == null){
			throw new Exception("Invalid request to store node group: comments are not provided");
		}
		if(getCreator() == null){
			throw new Exception("Invalid request to store node group: creator is not provided");
		}
		if(jsonRenderedNodeGroup == null || jsonRenderedNodeGroup.trim().isEmpty()){
			throw new Exception("Invalid request to store node group: node group is not provided, or is empty");
		}
	}
	
	public void setJsonRenderedNodeGroup(String jsonRenderedNodeGroup) {
		this.jsonRenderedNodeGroup = jsonRenderedNodeGroup;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getComments() {
		return comments;
	}
	
	public void setComments(String comments) {
		this.comments = comments;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}
	
}
